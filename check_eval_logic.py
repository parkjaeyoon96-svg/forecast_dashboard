import pandas as pd

# 평가율이 없을 때의 계산 확인
print('=' * 80)
print('평가율이 없을 때 재고평가감 환입 계산')
print('=' * 80)
print()

# 샘플 값
TAG매출 = 100000
표준매출원가 = 50000

print(f'샘플 값:')
print(f'  TAG매출: {TAG매출:,}')
print(f'  표준매출원가: {표준매출원가:,}')
print()

# 평가율이 0인 경우
평가율_0 = 0
result_0 = -TAG매출 * ((표준매출원가 / TAG매출 * 1.1) - 평가율_0) / 1.1
print(f'평가율이 0인 경우:')
print(f'  재고평가감 환입 = -{TAG매출:,} × (({표준매출원가:,} / {TAG매출:,} × 1.1) - {평가율_0}) / 1.1')
print(f'  = -{TAG매출:,} × ({표준매출원가 / TAG매출 * 1.1:.6f} - {평가율_0}) / 1.1')
print(f'  = {result_0:,.0f}')
print(f'  = -표준매출원가 ({-표준매출원가:,})')
print()

# 평가율이 있는 경우 (예: 15%)
평가율_15 = 0.15
result_15 = -TAG매출 * ((표준매출원가 / TAG매출 * 1.1) - 평가율_15) / 1.1
print(f'평가율이 15%인 경우:')
print(f'  재고평가감 환입 = -{TAG매출:,} × (({표준매출원가:,} / {TAG매출:,} × 1.1) - {평가율_15}) / 1.1')
print(f'  = -{TAG매출:,} × ({표준매출원가 / TAG매출 * 1.1:.6f} - {평가율_15}) / 1.1')
print(f'  = {result_15:,.0f}')
print()

# 평가율이 없을 때 재고평가감 환입을 0으로 처리하는 경우
print(f'평가율이 없을 때 재고평가감 환입을 0으로 처리:')
print(f'  재고평가감 환입 = 0')
print(f'  매출원가(평가감환입반영) = 표준매출원가 + 표준제간비 + 0')
print(f'  = 표준매출원가 + 표준제간비')
print()

